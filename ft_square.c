#include "libft.h"

size_t		ft_square(size_t n, size_t x)
{
	size_t	sum;

	sum = n;
	if (x == 0)
		return (1);
	while (x > 1)
	{
		sum = sum * n;
		x--;
	}
	return (sum);
}